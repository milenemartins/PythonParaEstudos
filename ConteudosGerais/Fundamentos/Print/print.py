# a função 'print()' é usada para exibir informações na saída/terminal
# ela permite que você veja os resultados de operações, visualize variáveis ou
# imprima mensagens de execução. Ele é muito utilizado para debugar o código

# segue exemplo básico de como é usado frequentemente
nome = "Heitor"
idade = 8
print(nome, idade)

# ao rodar o código, verá que ele retornou o valor que esta atribuido dentro da cada variável
