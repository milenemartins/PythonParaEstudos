# O loop "for" é utilizado para iterar sobre uma sequência (como uma lista, tupla, string, ou outra estrutura de dados iterável) ou outros objetos que suportam iteração. 
# O "for" é uma estrutura de controle de fluxo que permite executar um bloco de código várias vezes, uma vez para cada item na sequência.


# exemplo usado em uma lista
frutas = ["maçã", "banana", "laranja"]

for fruta in frutas: # o loop "for" itera sobre a lista de frutas e imprime cada fruta na saída
    print(fruta)

