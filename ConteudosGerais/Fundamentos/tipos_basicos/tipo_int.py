# aqui usamos a função 'type()' para retornar o tipo de objeto
# nesse caso eu quero saber qual o TIPO da variável 'ano_de_nascimento' e não o valor atribuido a ela

ano_de_nascimento = 2000 # variável 'ano_de_nascimento' com o valor '2000' atribuido
print(type(ano_de_nascimento)) # imprimindo o TIPO da variável
