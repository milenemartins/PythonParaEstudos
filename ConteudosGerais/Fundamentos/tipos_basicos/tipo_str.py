# aqui usamos a função 'type()' para retornar o tipo de objeto
# nesse caso eu quero saber qual o TIPO da variável 'nome_da_aluna' e não o valor atribuido a ela

nome_da_aluna = 'Alyce' # variável 'nome_da_aluna' com o valor 'Alyce' atribuido
print(type(nome_da_aluna)) # imprimindo o TIPO da variável