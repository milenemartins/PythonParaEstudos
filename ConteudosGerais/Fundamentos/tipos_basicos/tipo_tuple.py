# aqui usamos a função 'type()' para retornar o tipo de objeto
# nesse caso eu quero saber qual o TIPO da variável 'coordenadas' e não o valor atribuido a ela

coordenadas = (1, 2, 3, 4, 5) # variável 'coordenadas' com o valor '(1, 2, 3, 4, 5)' atribuido
print(type(coordenadas)) # imprimindo o TIPO da variável