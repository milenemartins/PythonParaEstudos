# aqui usamos a função 'type()' para retornar o tipo de objeto
# nesse caso eu quero saber qual o TIPO da variável 'conjunto_numeros' e não o valor atribuido a ela

conjunto_numeros = {1, 2, 3, 4, 5} # variável 'conjunto_numeros' com o valor '{1, 2, 3, 4, 5}' atribuido 
print(type(conjunto_numeros)) # imprimindo o TIPO da variável