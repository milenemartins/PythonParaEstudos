# aqui usamos a função 'type()' para retornar o tipo de objeto
# nesse caso eu quero saber qual o TIPO da variável 'dados_filme' e não o valor atribuido a ela

dados_filme = {"nome": "Zootopia", "ano": 2016, "genero": "Infantil"} # variável 'dados_filme' com o valor '{"nome": "Zootopia", "ano": 2016, "genero": "Infantil"}' atribuido 
print(type(dados_filme)) # imprimindo o TIPO da variável