# aqui usamos a função 'type()' para retornar o tipo de objeto
# nesse caso eu quero saber qual o TIPO da variável 'lista_numeros' e não o valor atribuido a ela

lista_numeros = [1, 2, 'melancia', 4, 5] # variável 'lista_numeros' com o valor '[1, 2, 3, 4, 5]' atribuido
print(type(lista_numeros)) # imprimindo o TIPO da variável