# existem duas formas de fazer comentários em um arquivo.py
# pode-se observar que ele é iniciado com o famoso caractere 'jogo da velha'
# os mesmos são utilizados para esclarecer o código e não são interpretados pela linguagem
# segue exemplo abaixo

idade = 10 # criando a variável 'idade'
print(idade) # imprimindo o valor da variável 'idade'

# ao rodar o código, verá que ele não retornará o comentário no terminal
# o python vai mostrar apenas o resultado que esta dentro da função 'print()'