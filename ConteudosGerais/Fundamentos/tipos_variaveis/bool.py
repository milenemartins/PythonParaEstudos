# a variável do tipo booleano(bool) é utilizada para armazenar um dos dois valores:
# seja ele True ou False. Elas são frequentemente usadas para representar condições lógicas

gosta_de_batata = True # criando a variável bool e atribuindo o valor 'True'
banana_vermelha = False # criando a variável bool e atribuindo o valor 'False'
print(gosta_de_batata, banana_vermelha) # imprimindo o valor das variáveis

