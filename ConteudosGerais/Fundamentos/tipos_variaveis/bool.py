# a variável dicionário é utilizada para apresentar uma estrutura de dados que armazena pares chave-valor
# cada chave em um dicionario é única e associada a um valor específico

dados_filme = {"nome": "Zootopia", "ano": 2016, "genero": "Infantil"} # criando a variável dict e atribuindo o valor 'dados_filme = {"nome": "Zootopia", "ano": 2016, "genero": "Infantil"}'
print(dados_filme) # imprimindo o valor da variável